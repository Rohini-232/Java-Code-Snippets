class Loops
{
 public static void main(String[] args) 
 { 
 
 // For loop
  for (int i = 1; i <5; i++) 
  {
   System.out.print(i+" ");   
  }
 // while loop   
int x=1;
  while(x<10)
  {
   System.out.println(x);
   x++;
  }
 //do while loop 
int a=1;
  do
  {
   System.out.print(a +" ");
   a++;
  }
  while(a<6); 

  // foreach loop
int ar[]={1,2,3,4,5};
  for (int element:ar) 
   System.out.print(element+" ");
  }
 }
